def resolve_coords(city: str) -> tuple[float, float] | None:
    """Resolve city to (lat, lon). Tries exact then partial match."""
    if city in CITY_COORDS:
        return CITY_COORDS[city]
    for key, coords in CITY_COORDS.items():
        if city in key or key in city:
            return coords
    return None


def compute_centroid(cities: list[str]) -> tuple[float, float] | None:
    """Compute average centroid for a list of cities."""
    points = []
    for city in cities:
        coords = resolve_coords(city)
        if coords:
            points.append(coords)
    if not points:
        return None
    avg_lat = sum(p[0] for p in points) / len(points)
    avg_lon = sum(p[1] for p in points) / len(points)
    return (avg_lat, avg_lon)
